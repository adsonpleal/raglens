// One-off: regenerate `src/addons/last-teleport/map-dimensions-data.ts`
// from OpenKore's FLD2 field files.
//
// Why OpenKore: rAthena's `db/re/map_cache.dat` shipped in the repo is
// a minimal test set (~8 maps). Real rAthena deployments rebuild it
// locally from the kRO .gat files, which aren't distributable.
// OpenKore's FLD2 set (~1300 maps) is the largest open-source dump of
// cell-accurate map dimensions.
//
// FLD2 layout (after gunzip):
//   u16 LE width   (cells)
//   u16 LE height  (cells)
//   u8  data[width * height]
//
// We only need the first 4 bytes per file. Top-level `fields/X.fld2.gz`
// is the canonical version; episode subdirs (`fields/EP14.1/`, etc.)
// fill in any maps the top-level set is missing.
//
// Usage:
//   curl -sL https://github.com/OpenKore/openkore/archive/refs/heads/master.tar.gz \
//     -o scripts/_openkore.tar.gz
//   (cd scripts && tar -xzf _openkore.tar.gz \
//     --wildcards 'openkore-master/fields/*.fld2.gz' \
//                 'openkore-master/fields/EP*/*.fld2.gz' \
//                 'openkore-master/fields/rRO/*.fld2.gz' \
//                 'openkore-master/fields/fRO/*.fld2.gz')
//   node scripts/extract-map-dimensions.mjs
//
// Writes `src/addons/last-teleport/map-dimensions-data.ts` directly.

import { readFileSync, readdirSync, statSync, writeFileSync } from "node:fs";
import { gunzipSync } from "node:zlib";
import { fileURLToPath } from "node:url";
import { dirname, join, basename } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const fieldsRoot = join(here, "openkore-master", "fields");
const outFile = join(
  here,
  "..",
  "src",
  "addons",
  "last-teleport",
  "map-dimensions-data.ts",
);

function walk(dir, out = []) {
  for (const name of readdirSync(dir)) {
    const p = join(dir, name);
    if (statSync(p).isDirectory()) walk(p, out);
    else if (name.endsWith(".fld2.gz")) out.push(p);
  }
  return out;
}

const files = walk(fieldsRoot);

const dims = new Map();
for (const file of files) {
  const mapName = basename(file).replace(/\.fld2\.gz$/, "");
  const isTopLevel = dirname(file) === fieldsRoot;
  const existing = dims.get(mapName);
  if (existing && (existing.topLevel || !isTopLevel)) continue;

  let header;
  try {
    const buf = readFileSync(file);
    const decoded = gunzipSync(buf);
    if (decoded.length < 4) continue;
    header = decoded.subarray(0, 4);
  } catch (e) {
    console.error(`skip ${file}: ${e.message}`);
    continue;
  }
  const width = header.readUInt16LE(0);
  const height = header.readUInt16LE(2);
  if (width === 0 || height === 0) continue;
  dims.set(mapName, { width, height, topLevel: isTopLevel });
}

const sorted = [...dims.entries()].sort(([a], [b]) => a.localeCompare(b));

const lines = [
  "// Auto-generated by scripts/extract-map-dimensions.mjs from OpenKore's",
  "// FLD2 field files. DO NOT EDIT BY HAND — re-run the script to refresh.",
  "",
  'import type { MapDimensions } from "./map-dimensions";',
  "",
  `// ${sorted.length} maps. Cell-accurate (width × height) extracted from`,
  "// each map's FLD2 header (u16 LE width, u16 LE height).",
  "export const KNOWN_MAP_DIMENSIONS: Record<string, MapDimensions> = {",
];
for (const [name, { width, height }] of sorted) {
  lines.push(`  ${JSON.stringify(name)}: { width: ${width}, height: ${height} },`);
}
lines.push("};", "");

writeFileSync(outFile, lines.join("\n"));
console.error(`wrote ${sorted.length} maps to ${outFile}`);
